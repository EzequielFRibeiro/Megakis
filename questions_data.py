# ====================== HELPERS ======================

def make_q(question, options, answer, diff=0):
    return {"q": question, "o": options, "a": answer, "d": diff}

def pick_distractors(correct, pool, n=3):
    others = [x for x in pool if x != correct]
    sample = random.sample(others, min(n, len(others)))
    opts = [correct] + sample
    random.shuffle(opts)
    return opts, opts.index(correct)

