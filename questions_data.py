def gen_pad(qs, facts, extra_concepts, extra_templates):
    result = list(qs)
    for idx, (q, o, a) in enumerate(facts):
        diff = 0 if idx < len(facts) * 0.33 else (1 if idx < len(facts) * 0.66 else 2)
        result.append(make_q(q, o, a, diff))
    extra = []
    for _ in range(500):
        term, defn = random.choice(extra_concepts)
        opts = [defn] + random.sample([d for t, d in extra_concepts if d != defn], min(3, len(extra_concepts)-1))
        random.shuffle(opts)
        ca = opts.index(defn)
        tmpl = random.choice(extra_templates)
        extra.append(make_q(tmpl(term), opts, ca, 2))
    result.extend(extra)
    random.shuffle(result)
    return result[:500]

