def gen_lang_questions(word_pairs, lang_name):
    qs = []
    pool_pt = [p[1] for p in word_pairs]
    pool_word = [p[0] for p in word_pairs]

    total = len(word_pairs)
    for idx, (word, meaning) in enumerate(word_pairs):
        diff = 0 if idx < total * 0.4 else (1 if idx < total * 0.75 else 2)
        opts, a = pick_distractors(meaning, pool_pt)
        qs.append(make_q(f"Qual a traducao de '{word.title()}' para portugues?", opts, a, diff))

    for idx, (word, meaning) in enumerate(word_pairs):
        diff = 0 if idx < total * 0.4 else (1 if idx < total * 0.75 else 2)
        opts, a = pick_distractors(word, pool_word)
        qs.append(make_q(f"Como se diz '{meaning.title()}' em {lang_name}?", opts, a, diff))

    random.shuffle(qs)
    return qs[:500]

